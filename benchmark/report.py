#!/usr/bin/env python3
"""
Benchmark Report Generator
===========================
Reads a benchmark results JSON and outputs:
  1. A summary table to stdout
  2. A markdown report saved to benchmark/results/

Usage
-----
    python -m benchmark.report benchmark/results/benchmark_20260714.json
    python -m benchmark.report --latest   # picks most recent file
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path

RESULTS_DIR = Path(__file__).parent / "results"

# Model size reference (for context in the report)
MODEL_INFO = {
    "tinyllama":  {"params": "1.1B", "size_gb": 0.64, "tier": "Speed"},
    "phi3:mini":  {"params": "3.8B", "size_gb": 2.30, "tier": "Balanced"},
    "mistral:7b": {"params": "7B",   "size_gb": 4.10, "tier": "Quality"},
}


def print_summary_table(results: dict) -> None:
    """Print a compact comparison table to stdout."""
    print(f"\n{'Model':<16} {'Params':<8} {'Size':<8} {'tok/s':<10} "
          f"{'TTFT(s)':<10} {'Avg Total(s)':<14} {'Tier'}")
    print("-" * 76)
    for run in results["runs"]:
        model = run["model"]
        info  = MODEL_INFO.get(model, {"params": "?", "size_gb": 0, "tier": "?"})
        print(
            f"{model:<16} {info['params']:<8} {info['size_gb']:<8.2f} "
            f"{run['mean_tokens_per_sec']:<10.1f} {run['mean_ttft_s']:<10.3f} "
            f"{run['mean_total_s']:<14.3f} {info['tier']}"
        )
    print()


def generate_markdown(results: dict) -> str:
    """Generate a full markdown benchmark report."""
    ts     = results.get("timestamp", "unknown")
    models = results.get("models", [])
    runs   = results.get("runs", [])

    lines = [
        "# DocMind — Local SLM Benchmark Report",
        "",
        f"**Run date:** {ts[:10]}  ",
        f"**Ollama endpoint:** {results.get('base_url', 'localhost')}  ",
        f"**Models tested:** {', '.join(f'`{m}`' for m in models)}",
        "",
        "---",
        "",
        "## Summary",
        "",
        "| Model | Params | Size | tok/s | TTFT (s) | Avg Total (s) | Tier |",
        "|-------|--------|------|-------|----------|---------------|------|",
    ]

    for run in runs:
        model = run["model"]
        info  = MODEL_INFO.get(model, {"params": "?", "size_gb": 0, "tier": "?"})
        lines.append(
            f"| `{model}` | {info['params']} | {info['size_gb']:.2f} GB "
            f"| **{run['mean_tokens_per_sec']:.1f}** "
            f"| {run['mean_ttft_s']:.3f} "
            f"| {run['mean_total_s']:.3f} "
            f"| {info['tier']} |"
        )

    lines += [
        "",
        "> **tok/s** = tokens generated per second (from Ollama `eval_duration`)  ",
        "> **TTFT** = time-to-first-token (from Ollama `prompt_eval_duration`)  ",
        "> **Avg Total** = mean wall-clock time across all prompts  ",
        "",
        "---",
        "",
        "## Per-Prompt Breakdown",
        "",
    ]

    for run in runs:
        model = run["model"]
        lines += [f"### `{model}`", ""]
        lines += [
            "| Prompt | Category | tok/s | TTFT (s) | Total (s) | Tokens |",
            "|--------|----------|-------|----------|-----------|--------|",
        ]
        for r in run["prompt_results"]:
            if "error" in r:
                lines.append(f"| {r['prompt_id']} | {r['category']} | ERROR | — | — | — |")
            else:
                lines.append(
                    f"| {r['prompt_id']} | {r['category']} "
                    f"| {r['tokens_per_sec']:.1f} "
                    f"| {r['ttft_s']:.3f} "
                    f"| {r['total_s']:.3f} "
                    f"| {r['tokens_generated']} |"
                )
        lines.append("")

    lines += [
        "---",
        "",
        "## Quality vs Speed Tradeoff",
        "",
        "| Model | Speed | Quality | Privacy | RAM | Best for |",
        "|-------|-------|---------|---------|-----|----------|",
        "| `tinyllama` | ⭐⭐⭐ | ⭐ | ✅ 100% local | <1 GB | Rapid prototyping, edge devices |",
        "| `phi3:mini` | ⭐⭐ | ⭐⭐⭐ | ✅ 100% local | ~2.3 GB | Production balance — best overall |",
        "| `mistral:7b` | ⭐ | ⭐⭐⭐ | ✅ 100% local | ~4.1 GB | Highest quality, latency-tolerant tasks |",
        "",
        "All models run **100% locally** — zero data sent to any external server.",
        "Cost per query: **£0.00**.",
        "",
        "---",
        "",
        "_Generated by `benchmark/report.py`_",
    ]

    return "\n".join(lines)


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate benchmark markdown report")
    parser.add_argument("results_file", nargs="?", default=None)
    parser.add_argument("--latest", action="store_true",
                        help="Use the most recent results file")
    parser.add_argument("--output", default=None)
    args = parser.parse_args()

    if args.latest or args.results_file is None:
        files = sorted(RESULTS_DIR.glob("benchmark_*.json"))
        if not files:
            print("No results files found. Run benchmark/run_benchmark.py first.")
            return
        results_path = files[-1]
    else:
        results_path = Path(args.results_file)

    print(f"Reading: {results_path}")
    results = json.loads(results_path.read_text())

    print_summary_table(results)

    md = generate_markdown(results)
    out = args.output or str(results_path).replace(".json", "_report.md")
    Path(out).write_text(md, encoding="utf-8")
    print(f"Markdown report saved → {out}")


if __name__ == "__main__":
    main()
